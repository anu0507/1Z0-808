package ControlFlow;

public class Student {
	private int id;
	private String name;
	private float tenPercentage;
	private float twelvePercentage;
	private float cgpa;
	private boolean hasBacklog;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public float getTenPercentage() {
		return tenPercentage;
	}

	public void setTenPercentage(float tenPercentage) {
		this.tenPercentage = tenPercentage;
	}

	public float getTwelvePercentage() {
		return twelvePercentage;
	}

	public void setTwelvePercentage(float twelvePercentage) {
		this.twelvePercentage = twelvePercentage;
	}

	public float getCgpa() {
		return cgpa;
	}

	public void setCgpa(float cgpa) {
		this.cgpa = cgpa;
	}

	public boolean isHasBacklog() {
		return hasBacklog;
	}

	public void setHasBacklog(boolean hasBacklog) {
		this.hasBacklog = hasBacklog;
	}

}
